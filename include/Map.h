#ifndef MAP_H
#define MAP_H

#include "MapPoint.h"
#include "KeyFrame.h"
#include <set>

#include <mutex>



namespace ORB_SLAM2
{

class MapPoint;
class KeyFrame;

class Map
{
public:
    Map();

    void AddKeyFrame(KeyFrame* pKF);    // 新增关键帧
    void AddMapPoint(MapPoint* pMP);    // 新增地图点
    void EraseKeyFrame(KeyFrame* pKF);  // 删除关键帧
    void EraseMapPoint(MapPoint* pMP);  // 删除地图点
    void SetReferenceMapPoints(const std::vector<MapPoint*> &vpMPs);// 设置参考地图点
    void InformNewBigChange();          // 通知有大的改变
    int GetLastBigChangeIdx();          // 获取最后的大改变索引

    std::vector<KeyFrame*> GetAllKeyFrames();       // 获取所有关键帧
    std::vector<MapPoint*> GetAllMapPoints();       // 获取所有地图点
    std::vector<MapPoint*> GetReferenceMapPoints(); // 获取参考地图点

    long unsigned int MapPointsInMap();
    long unsigned  KeyFramesInMap();

    long unsigned int GetMaxKFid();

    void clear();

    vector<KeyFrame*> mvpKeyFrameOrigins;

    std::mutex mMutexMapUpdate;

    // This avoid that two points are created simultaneously in separate threads (id conflict)
    std::mutex mMutexPointCreation;

protected:
    std::set<MapPoint*> mspMapPoints;
    std::set<KeyFrame*> mspKeyFrames;

    std::vector<MapPoint*> mvpReferenceMapPoints;

    long unsigned int mnMaxKFid;

    // Index related to a big change in the map (loop closure, global BA)
    int mnBigChangeIdx;

    std::mutex mMutexMap;
};

} //namespace ORB_SLAM

#endif // MAP_H
